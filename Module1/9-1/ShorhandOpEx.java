package tops.a91;

public class ShorhandOpEx 
{
	public static void main(String[] args) 
	{
		int a = 6;
		int b = 3;
		
		a+=b; // a = a + b //6+3
		System.out.println(a);
		
		a-=b; // a = a - b //9-3
		System.out.println(a);//6
		
		a*=b;
		System.out.println(a);
		
		
		
	
	}
}

