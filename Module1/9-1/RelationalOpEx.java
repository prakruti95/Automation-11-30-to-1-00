package tops.a91;

public class RelationalOpEx 
{
	public static void main(String[] args) 
	{
		int a = 9;
		int b = 5;
		int c = 9;
		
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=c);
		System.out.println(a==b);
		System.out.println(a==c);
		System.out.println(a!=c);
	}
}
